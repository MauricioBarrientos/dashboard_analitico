import React, { ButtonHTMLAttributes, ForwardRefRenderFunction } from 'react';
import clsx from 'clsx';

interface ButtonProps extends ButtonHTMLAttributes<HTMLButtonElement> {
  variant?: 'primary' | 'secondary' | 'outline' | 'ghost';
  size?: 'sm' | 'md' | 'lg';
  isLoading?: boolean;
}

const ButtonWithRef: ForwardRefRenderFunction<HTMLButtonElement, ButtonProps> = (
  { variant = 'primary', size = 'md', isLoading, className, children, disabled, ...props },
  ref
) => {
  const baseClasses = 'inline-flex items-center justify-center rounded-lg font-medium transition-all duration-200 focus:outline-none focus:ring-2 focus:ring-offset-2 disabled:opacity-50 disabled:pointer-events-none shadow-sm';

  const variantClasses = clsx({
    'bg-blue-600 text-white hover:bg-blue-700 focus:ring-blue-500 hover:shadow-md': variant === 'primary',
    'bg-gray-200 text-gray-800 hover:bg-gray-300 focus:ring-gray-500 dark:bg-gray-700 dark:text-white dark:hover:bg-gray-600': variant === 'secondary',
    'border border-gray-300 text-gray-700 hover:bg-gray-50 focus:ring-gray-500 dark:border-gray-600 dark:text-gray-300 dark:hover:bg-gray-700': variant === 'outline',
    'text-gray-700 hover:bg-gray-100 focus:ring-gray-500 dark:text-gray-300 dark:hover:bg-gray-700': variant === 'ghost',
  });

  const sizeClasses = clsx({
    'text-xs py-1.5 px-3': size === 'sm',
    'text-sm py-2 px-4': size === 'md',
    'text-base py-2.5 px-5': size === 'lg',
  });

  const classes = clsx(
    baseClasses,
    variantClasses,
    sizeClasses,
    className
  );

  return (
    <button
      ref={ref}
      className={classes}
      disabled={disabled || isLoading}
      aria-busy={isLoading}
      {...props}
    >
      {isLoading ? (
        <>
          <svg className="animate-spin -ml-1 mr-2 h-4 w-4 text-current" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
            <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
            <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
          </svg>
          Cargando...
        </>
      ) : children}
    </button>
  );
};

const Button = React.forwardRef(ButtonWithRef);
Button.displayName = 'Button';

export default Button;